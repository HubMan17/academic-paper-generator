def get_mock_for_section(section_key: str, title: str = "") -> str:
    """Get appropriate mock text for a section by its key."""
    if section_key == "intro":
        return MOCK_INTRO_TEXT
    elif section_key == "conclusion":
        return MOCK_CONCLUSION_TEXT
    elif section_key.startswith("theory"):
        return MOCK_THEORY_TEXT
    elif section_key.startswith("practice"):
        return MOCK_PRACTICE_TEXT
    else:
        return MOCK_SECTION_TEXT.format(title=title or section_key, key=section_key)
